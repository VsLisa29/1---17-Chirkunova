n1 = input("Выберете первый цвет для смешивания: красный, синий, желтый: ")
n2 = input("Выберете второй цвет для смешивания: красный, синий, желтый: ")

if (n1 == "красный" and  n2 == "синий") or (n1 == "синий" and n2 == "красный"):
    print("фиолетовый")
elif (n1 == "красный" and  n2 == "желтый") or (n1 == "желтый" and n2 == "красный"):
    print("оранжевый")
elif (n1 == "синий" and  n2 == "желтый") or (n1 == "синий" and n2 == "красный"):
    print("зеленый")
else:
    print("Ошибка!")